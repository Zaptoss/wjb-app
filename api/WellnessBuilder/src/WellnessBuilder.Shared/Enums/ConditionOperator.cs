namespace WellnessBuilder.Shared.Enums;

public enum ConditionOperator
{
    /// <summary>
    /// Equals
    /// </summary>
    Eq,

    /// <summary>
    /// Not equals
    /// </summary>
    NotEq,

    /// <summary>
    /// In range
    /// </summary>
    In,

    /// <summary>
    /// Greater than
    /// </summary>
    Gt,

    /// <summary>
    /// Greater than or equals
    /// </summary>
    Gte,

    /// <summary>
    /// Lower then
    /// </summary>
    Lt,

    /// <summary>
    /// Lower than or equals
    /// </summary>
    Lte,

    /// <summary>
    /// Not in range
    /// </summary>
    Nin,

    /// <summary>
    /// Contains substring
    /// </summary>
    Contains
}
